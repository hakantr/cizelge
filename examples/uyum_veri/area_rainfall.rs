pub fn verileri_çöz() -> Result<(Vec<f64>, Vec<f64>), String> {
    Ok((
        binde_bir_delta_verisini_çöz("Flow", AKIŞ_KODU)?,
        binde_bir_delta_verisini_çöz("Rainfall", YAĞIŞ_KODU)?,
    ))
}

fn binde_bir_delta_verisini_çöz(ad: &str, kod: &str) -> Result<Vec<f64>, String> {
    let baytlar = base64::engine::general_purpose::STANDARD
        .decode(kod.as_bytes())
        .map_err(|hata| format!("{ad} fixture verisi base64 çözülemedi: {hata}"))?;
    let mut sonuç = Vec::with_capacity(VERİ_SAYISI);
    let mut önceki = 0_i64;
    let mut sıra = 0_usize;

    while sıra < baytlar.len() {
        let mut zigzag = 0_u64;
        let mut kaydırma = 0_u32;
        loop {
            let bayt = *baytlar
                .get(sıra)
                .ok_or_else(|| format!("{ad} fixture verisinde yarım varint"))?;
            sıra += 1;
            if kaydırma >= 64 || (kaydırma == 63 && bayt & 0x7e != 0) {
                return Err(format!("{ad} fixture verisinde taşan varint"));
            }
            zigzag |= u64::from(bayt & 0x7f) << kaydırma;
            if bayt & 0x80 == 0 {
                break;
            }
            kaydırma += 7;
        }

        let büyüklük = i64::try_from(zigzag >> 1)
            .map_err(|_| format!("{ad} fixture deltası i64 sınırını aşıyor"))?;
        let fark = if zigzag & 1 == 0 {
            büyüklük
        } else {
            -büyüklük - 1
        };
        önceki = önceki
            .checked_add(fark)
            .ok_or_else(|| format!("{ad} fixture toplamı i64 sınırını aşıyor"))?;
        sonuç.push(önceki as f64 / 1_000.0);
    }

    if sonuç.len() != VERİ_SAYISI {
        return Err(format!(
            "{ad} fixture uzunluğu {} olmalıydı, {} bulundu",
            VERİ_SAYISI,
            sonuç.len()
        ));
    }
    Ok(sonuç)
}

