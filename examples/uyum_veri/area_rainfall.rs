    #[test]
    fn resmi_diziler_kayipsiz_cozulur() {
        let (akış, yağış) = verileri_çöz().expect("fixture çözülmeli");
        assert_eq!(akış.len(), 3_079);
        assert_eq!(yağış.len(), 3_079);
        assert_eq!(akış.iter().copied().reduce(f64::max), Some(365.0));
        assert_eq!(yağış.iter().copied().reduce(f64::max), Some(9.415));
    }
